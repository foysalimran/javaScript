 /*
    PRIMITIVE DATA TYPES
    
    1. Number: Floating point numbers, for decimals and integers.
    2. String: Sequence of characters, used for text.
    3. Boolean: Logical data type that can only be true or false
    4. Undefined: Data type of a variable that does not have a value yet.
    5. Null: Also means 'not-existent'.

    Note: Javascript has dynamic typing: data types are automatically assigned to variables.
 */


 var firstName = 'John';
 console.log(firstName);

 var lastName = 'Smith'; // String
 var age = 28; // Number

 var fullAge = true; // Boolean
 
 console.log(fullAge);

var job; // Undefined
console.log(job);

job = 'Teacher';
console.log(job);


