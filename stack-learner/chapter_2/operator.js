// Arithmetic Operator
// + - * ** / % ++ --

var a = 11;
var b = 3;

console.log(a % b);

// Incremental Operator
// Pre Incremental  - Post Incremental

console.log(++a); // Pre Increment

console.log(a++); // Post Increment
console.log(a);

console.log(--b); // Pre decrement
console.log(b--); //Post decrement
console.log(b);
